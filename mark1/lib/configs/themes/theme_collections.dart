import 'package:flex_color_scheme/flex_color_scheme.dart';
import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';

class ThemeCollections {
  ThemeCollections._(); // Private constructor to prevent instantiation

  static const List<String> availableThemes = [
    'original',
    'seaTheSky',
    "inTheForest"
  ];
  static final List<List> themeMapping = [
    [originalThemeLight, originalThemeDark],
    [seaTheSkyThemeLight, seaTheSkyThemeDark],
    [inTheForestThemeLight, inTheForestThemeDark]
  ];

  // Define common seaTheSky, inTheForest, and LukeCreated Original themes settings
  static const _surfaceMode = FlexSurfaceMode.levelSurfacesLowScaffold;
  static const _blendLevel = 7;
  static const _subThemesData = FlexSubThemesData(
    blendOnLevel: 10,
    blendOnColors: false,
    useTextTheme: true,
    useM2StyleDividerInM3: true,
    alignedDropdown: true,
    useInputDecoratorThemeInDialogs: true,
  );
  static final _visualDensity = FlexColorScheme.comfortablePlatformDensity;
  static const _useMaterial3 = true;
  static const _swapLegacyOnMaterial3 = true;

  // Define font families for all themes
  static final _fontFamily = GoogleFonts.chewy().fontFamily;
  static final _fontFamilyFallback = <String>[
    GoogleFonts.krub().fontFamily!,
    GoogleFonts.maShanZheng().fontFamily!,
    GoogleFonts.notoSans().fontFamily!
  ];

  // Helper method to create ThemeData
  static ThemeData _createThemeData({
    required FlexSchemeColor colors,
    required Brightness brightness,
  }) {
    return FlexThemeData.light(
      colors: colors,
      surfaceMode: _surfaceMode,
      blendLevel: _blendLevel,
      subThemesData: _subThemesData,
      visualDensity: _visualDensity,
      useMaterial3: _useMaterial3,
      swapLegacyOnMaterial3: _swapLegacyOnMaterial3,
      fontFamily: _fontFamily,
      fontFamilyFallback: _fontFamilyFallback,
    ).copyWith(
      brightness: brightness,
    );
  }

  // Animation curve for theme transitions
  static const themeChangeAnimationCurve = Curves.easeInOut;
  static const themeChangeAnimationDuration = Duration(milliseconds: 300);

  // Method to get a theme by name
  static ThemeData getThemeByName(String name, {required bool isDark}) {
    for (int i = 0; i < availableThemes.length; i++) {
      if (availableThemes.elementAt(i).contains(name)) {
        return isDark
            ? themeMapping.elementAt(i)[1]
            : themeMapping.elementAt(i)[0];
      }
    }

    availableThemes.elementAt(0);
    return isDark ? seaTheSkyThemeDark : seaTheSkyThemeLight;
  }

  // Create LukeCreated Original Light theme data
  static final originalThemeLight = _createThemeData(
    colors: _originalLightColors,
    brightness: Brightness.light,
  );

  // Create LukeCreated Original Dark theme data
  static final originalThemeDark = _createThemeData(
    colors: _originalDarkColors,
    brightness: Brightness.dark,
  );

  // Create seaTheSky Light theme data
  static final seaTheSkyThemeLight = _createThemeData(
    colors: _seaTheSkyLightColors,
    brightness: Brightness.light,
  );

  // Create seaTheSky Dark theme data
  static final seaTheSkyThemeDark = _createThemeData(
    colors: _seaTheSkyDarkColors,
    brightness: Brightness.dark,
  );

  // Create inTheForest Light theme data
  static final inTheForestThemeLight = _createThemeData(
    colors: _inTheForestLightColors,
    brightness: Brightness.light,
  );

  // Create inTheForest Dark theme data
  static final inTheForestThemeDark = _createThemeData(
    colors: _inTheForestDarkColors,
    brightness: Brightness.dark,
  );

